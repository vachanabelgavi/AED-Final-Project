    public void populateOrders() {
        tableModel.setRowCount(0);
        try {
            for (Order o : this.customer.getOrderlist()) {
                //                populate items
               
                    tableModel.insertRow(tableModel.getRowCount(), new Object[]{
                        o.getOrderId(),
                        o.getStatus(),
                        o.getOrderPayment().getStatus(),
                        o.getPrice()
                    });

                
                
            }

        } catch (Exception e) {

        }

    }

